    private void colorFondoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_colorFondoItemStateChanged
        // TODO add your handling code here:
        
        if(evt.getItem() instanceof String) {
            String color = (String) evt.getItem();
            
            switch(color) {
                case "Blanco":
                    estela.setColorFondo(Color.WHITE);
                    break;
                case "Rosa":
                    estela.setColorFondo(Color.PINK);
                    break;
                case "Azul":
                    estela.setColorFondo(Color.BLUE);
                    break;
                case "Verde":
                    estela.setColorFondo(Color.GREEN);
                    break;
                case "Amarillo":
                    estela.setColorFondo(Color.YELLOW);
                    break;
            }
        }

    }//GEN-LAST:event_colorFondoItemStateChanged

    private void estelaMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_estelaMouseMoved

        // TODO add your handling code here:
        estela.add(evt.getX(), evt.getY());
        repaint();
        
    }//GEN-LAST:event_estelaMouseMoved

    private void colorEstelaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_colorEstelaItemStateChanged
        // TODO add your handling code here:
        
        if(evt.getItem() instanceof String) {
            String color = (String) evt.getItem();
            
            switch(color) {
                case "Negro":
                    estela.setColorFigura(Color.BLACK);
                    break;
                case "Rosa":
                    estela.setColorFigura(Color.PINK);
                    break;
                case "Azul":
                    estela.setColorFigura(Color.BLUE);
                    break;
                case "Verde":
                    estela.setColorFigura(Color.GREEN);
                    break;
                case "Amarillo":
                    estela.setColorFigura(Color.YELLOW);
                    break;
            }
        }
        
    }//GEN-LAST:event_colorEstelaItemStateChanged

    private void formaEstelaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_formaEstelaItemStateChanged
        // TODO add your handling code here:
        
        if(evt.getItem() instanceof String) {
            String figura = (String) evt.getItem();
            estela.setFigura(figura);
        }
        
    }//GEN-LAST:event_formaEstelaItemStateChanged

    private void estelaMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_estelaMouseExited
        // TODO add your handling code here:
        estela.vaciar();
        repaint();
    }//GEN-LAST:event_estelaMouseExited

